package com.vinsguru.grpc.helperModel;

public class QRModel {
    private String qrCode;
    private String qrCodeKey;
    private String qrCodeSetup;

    public String getQrCode() {
        return qrCode;
    }

    public void setQrCode(String qrCode) {
        this.qrCode = qrCode;
    }

    public String getQrCodeKey() {
        return qrCodeKey;
    }

    public void setQrCodeKey(String qrCodeKey) {
        this.qrCodeKey = qrCodeKey;
    }

    public String getQrCodeSetup() {
        return qrCodeSetup;
    }

    public void setQrCodeSetup(String qrCodeSetup) {
        this.qrCodeSetup = qrCodeSetup;
    }
}
